package com.lovdmx.control.vo;

import java.util.List;

public class TaskPauseOrRestoreVo {

	// uuid
	private String uuid;

	// 任务类型(0:暂停 1:恢复)
	private String taskType;
	// 任务md5值
	private List<String> taskMd5s;

	public TaskPauseOrRestoreVo() {

	}

	public TaskPauseOrRestoreVo(String uuid, String taskType) {
		super();
		this.uuid = uuid;
		this.taskType = taskType;
	}

	public TaskPauseOrRestoreVo(String uuid, String taskType, List<String> taskMd5s) {
		super();
		this.uuid = uuid;
		this.taskType = taskType;
		this.taskMd5s = taskMd5s;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getTaskType() {
		return taskType;
	}

	public void setTaskType(String taskType) {
		this.taskType = taskType;
	}

	public List<String> getTaskMd5s() {
		return taskMd5s;
	}

	public void setTaskMd5s(List<String> taskMd5s) {
		this.taskMd5s = taskMd5s;
	}

}
