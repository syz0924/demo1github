public class TimedTaskVo {
	// 任务名
	private String taskName;
	// 任务Md5值
	private String taskMd5;
	// 任务类型
	private Integer taskType;
	// 循环模式
	private Integer cyclicMode;
	// 循环日期
	private String cyclicDate;
	// 开始日期
	private String startDate;
	// 结束日期
	private String endDate;
	// 开始时间
	private String startTime;
	// 结束时间
	private String endTime;
	// uuid
	private String uuid;
	// 播放列表
	// private String playList;
	// 视频任务信息
	private List<TaskFileTypeInfoVo> taskList;

	public TimedTaskVo(String taskName, String taskMd5, Integer taskType, Integer cyclicMode, String cyclicDate,
			String startDate, String endDate, String startTime, String endTime, List<TaskFileTypeInfoVo> taskList,
			String uuid) {
		super();
		this.taskName = taskName;
		this.taskMd5 = taskMd5;
		this.taskType = taskType;
		this.cyclicMode = cyclicMode;
		this.cyclicDate = cyclicDate;
		this.startDate = startDate;
		this.endDate = endDate;
		this.startTime = startTime;
		this.endTime = endTime;
		this.taskList = taskList;
		this.uuid = uuid;
	}

	public TimedTaskVo() {
		super();
	}

	public String getTaskName() {
		return taskName;
	}

	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}

	public String getTaskMd5() {
		return taskMd5;
	}

	public void setTaskMd5(String taskMd5) {
		this.taskMd5 = taskMd5;
	}

	public Integer getTaskType() {
		return taskType;
	}

	public void setTaskType(Integer taskType) {
		this.taskType = taskType;
	}

	public Integer getCyclicMode() {
		return cyclicMode;
	}

	public void setCyclicMode(Integer cyclicMode) {
		this.cyclicMode = cyclicMode;
	}

	public String getCyclicDate() {
		return cyclicDate;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public void setCyclicDate(String cyclicDate) {
		this.cyclicDate = cyclicDate;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public List<TaskFileTypeInfoVo> getTaskList() {
		return taskList;
	}

	public void setTaskList(List<TaskFileTypeInfoVo> taskList) {
		this.taskList = taskList;
	}

}
